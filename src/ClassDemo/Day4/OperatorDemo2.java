package ClassDemo.Day4;

public class OperatorDemo2 {
    public static void main(String[] args) {
        //Relational Operators: >, <, >=, <=, ++, !=
        int a=5;
        int b=6;
        System.out.println(a>b);  // false
        System.out.println(a<b);  // true
        System.out.println(a>=b); // false
        System.out.println(a<=b); // true
        System.out.println(a==b); // false
        System.out.println(a!=b); // true

    }
}
